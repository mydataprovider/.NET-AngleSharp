using System;

namespace AngleSharp.DOM.Html
{
    /// <summary>
    /// A list of possible quirks mode states.
    /// </summary>
    public enum QuirksMode
    {
        /// <summary>
        /// The quirks mode is deactivated.
        /// </summary>
        Off,
        /// <summary>
        /// The quirks mode is partly activated.
        /// </summary>
        Limited,
        /// <summary>
        /// The quirks mode is activated.
        /// </summary>
        On
    }
}
